#include "../push_swap.h"

void	swap(t_list **lst)
{
	t_list	*temp;
	t_list	*aux;

	temp = *lst;
	aux = (*lst)->next;
	temp->next = aux->next;
	aux->next = temp;
	*lst = aux;
	ft_set_pos(lst);
}

void	sa(t_list **a)
{
	swap(a);
	write(1, "sa\n", 3);
}

void	sb(t_list **b)
{
	swap(b);
	write(1, "sb\n", 3);
}

void	ss(t_list **a, t_list **b)
{
	swap(a);
	swap(b);
	write(1, "ss\n", 3);
}
